public class IntTest01 {
    public static void main(String[] args) {
        //十进制
        int a = 10;
        System.out.println(a);

        //八进制
        int b = 010;
        System.out.println(b);

        //十六进制
        int c = 0x10;
        System.out.println(c);
        int x = 16;
        System.out.println(x);

        //二进制
        int d = 0b10;
        System.out.println(d);
    }
}
